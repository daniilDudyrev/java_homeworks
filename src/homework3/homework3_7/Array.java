package homework3.homework3_7;

class Array implements ISort {
    private int[] arr;

    public Array(int[] arr) {
        this.arr = arr;
    }

    @Override
    public void SortAsc() {
        for (int i = 0; i < arr.length - 1; i++) {
            for (int j = 0; j < arr.length - i - 1; j++) {
                if (arr[j] > arr[j + 1]) {
                    int temp = arr[j];
                    arr[j] = arr[j + 1];
                    arr[j + 1] = temp;
                }
            }
        }
    }

    @Override
    public void SortDesc() {
        for (int i = 0; i < arr.length - 1; i++) {
            for (int j = 0; j < arr.length - i - 1; j++) {
                if (arr[j] < arr[j + 1]) {
                    int temp = arr[j];
                    arr[j] = arr[j + 1];
                    arr[j + 1] = temp;
                }
            }
        }
    }

    public void PrintArray() {
        for (int num : arr) {
            System.out.print(num + " ");
        }
        System.out.println();
    }
}